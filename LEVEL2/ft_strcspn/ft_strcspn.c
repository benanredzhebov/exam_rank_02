#include <stdio.h>
// #include <string.h>

size_t	ft_strcspn(const char *s, const char *reject)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[1] != '\0')
	{
		j = 0;
		while (reject[j] != '\0')
		{
			if (s[i] == reject[j])
				return (i);
			j++;
		}
		i++;
	}
	return (i);
}

int main() {
	const	char *str1 = "Hello, World!";
	const	char *str2 = ", ";

	size_t	result = ft_strcspn(str1, str2);

	printf("Result: %zu\n", result);

	return 0;
}